package OOP.ClassNObject;

public class Fun {
    String name;
    String color;
    String title;
    public static void main(String[] args){
        Fun fun=new Fun();
        fun.name="CHEVROLET";
        fun.color="RED";
        fun.title="CAMARO";
        System.out.println("Name: "+fun.name);
        System.out.println("Color: "+fun.color);
        System.out.println("Title: "+fun.title);
    }
}